'use strict';

/**
 * Downloads the woff2 files Waterline needs (Bricolage Grotesque, Figtree,
 * Spline Sans Mono) from Google Fonts at BUILD time and writes a local,
 * CSP-safe assets/fonts/fonts.css. The app never hits the network at runtime.
 *
 * If the network is unavailable, this writes a fonts.css that defines nothing
 * and the UI falls back to the system font stacks declared in styles.css.
 *
 *   node build/fetch-fonts.js
 */

const fs = require('fs');
const path = require('path');
const https = require('https');

const FONTS_DIR = path.join(__dirname, '..', 'assets', 'fonts');
const CSS_URL =
  'https://fonts.googleapis.com/css2' +
  '?family=Bricolage+Grotesque:wght@600;800' +
  '&family=Figtree:wght@400;500;600' +
  '&family=Spline+Sans+Mono:wght@500' +
  '&display=swap';

// A desktop-Chrome UA makes Google Fonts return modern woff2.
const UA =
  'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 ' +
  '(KHTML, like Gecko) Chrome/124.0.0.0 Safari/537.36';

const KEEP_SUBSETS = new Set(['latin', 'latin-ext']);

function get(url, { binary = false } = {}) {
  return new Promise((resolve, reject) => {
    const req = https.get(url, { headers: { 'User-Agent': UA } }, (res) => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        res.resume();
        return resolve(get(res.headers.location, { binary }));
      }
      if (res.statusCode !== 200) {
        res.resume();
        return reject(new Error(`HTTP ${res.statusCode} for ${url}`));
      }
      const chunks = [];
      res.on('data', (c) => chunks.push(c));
      res.on('end', () => resolve(binary ? Buffer.concat(chunks) : Buffer.concat(chunks).toString('utf8')));
    });
    req.on('error', reject);
    req.setTimeout(15000, () => req.destroy(new Error('timeout')));
  });
}

function parseFaces(css) {
  // Each @font-face is preceded by a `/* subset */` comment in Google's output.
  const faces = [];
  const re = /\/\*\s*([\w-]+)\s*\*\/\s*@font-face\s*\{([^}]+)\}/g;
  let m;
  while ((m = re.exec(css))) {
    const subset = m[1];
    const body = m[2];
    const family = (body.match(/font-family:\s*'([^']+)'/) || [])[1];
    const weight = (body.match(/font-weight:\s*(\d+)/) || [])[1] || '400';
    const style = (body.match(/font-style:\s*(\w+)/) || [])[1] || 'normal';
    const url = (body.match(/src:\s*url\(([^)]+)\)/) || [])[1];
    const range = (body.match(/unicode-range:\s*([^;]+);/) || [])[1];
    if (family && url) faces.push({ subset, family, weight, style, url, range });
  }
  return faces;
}

async function main() {
  fs.mkdirSync(FONTS_DIR, { recursive: true });
  let css;
  try {
    css = await get(CSS_URL);
  } catch (err) {
    console.warn('[fetch-fonts] Could not reach Google Fonts:', err.message);
    console.warn('[fetch-fonts] Writing empty fonts.css; app will use system fallbacks.');
    fs.writeFileSync(
      path.join(FONTS_DIR, 'fonts.css'),
      '/* Fonts unavailable at build time — using system fallbacks. */\n'
    );
    return;
  }

  const faces = parseFaces(css).filter((f) => KEEP_SUBSETS.has(f.subset));
  const out = ['/* Generated by build/fetch-fonts.js — bundled woff2, no network at runtime. */', ''];
  let ok = 0;

  for (const f of faces) {
    const slug = f.family.replace(/\s+/g, '-');
    const file = `${slug}-${f.weight}-${f.subset}.woff2`;
    try {
      const data = await get(f.url, { binary: true });
      fs.writeFileSync(path.join(FONTS_DIR, file), data);
      ok++;
      out.push('@font-face {');
      out.push(`  font-family: '${f.family}';`);
      out.push(`  font-style: ${f.style};`);
      out.push(`  font-weight: ${f.weight};`);
      out.push('  font-display: swap;');
      out.push(`  src: url('./${file}') format('woff2');`);
      if (f.range) out.push(`  unicode-range: ${f.range.trim()};`);
      out.push('}');
      out.push('');
    } catch (err) {
      console.warn(`[fetch-fonts] Skipped ${file}: ${err.message}`);
    }
  }

  fs.writeFileSync(path.join(FONTS_DIR, 'fonts.css'), out.join('\n'));
  console.log(`[fetch-fonts] Wrote ${ok} woff2 file(s) + fonts.css`);
}

main().catch((err) => {
  console.error('[fetch-fonts] Unexpected error:', err);
  // Never fail the build over fonts.
  try {
    fs.mkdirSync(FONTS_DIR, { recursive: true });
    fs.writeFileSync(path.join(FONTS_DIR, 'fonts.css'), '/* Font fetch failed — system fallbacks. */\n');
  } catch (_) {}
});
